TEST(DilithiumAvx2VerifyTest, InvalidPublicKeys) {
  if (IsFipsModeEnabled()) {
    GTEST_SKIP() << "Test assumes kOnlyUseFips is false.";
  }

  // Null public key.
  const absl::string_view null_public_key;
  EXPECT_FALSE(DilithiumAvx2Verify::New(
                   *DilithiumPublicKeyPqclean::NewPublicKey(null_public_key))
                   .ok());

  for (int keysize = 0; keysize < PQCLEAN_DILITHIUM2_AVX2_CRYPTO_PUBLICKEYBYTES;
       keysize++) {
    if (keysize == PQCLEAN_DILITHIUM2_AVX2_CRYPTO_PUBLICKEYBYTES ||
        keysize == PQCLEAN_DILITHIUM3_AVX2_CRYPTO_PUBLICKEYBYTES ||
        keysize == PQCLEAN_DILITHIUM5_AVX2_CRYPTO_PUBLICKEYBYTES) {
      // Valid key size.
      continue;
    }
    std::string key(keysize, 'x');
    EXPECT_FALSE(
        DilithiumAvx2Verify::New(*DilithiumPublicKeyPqclean::NewPublicKey(key))
            .ok());
  }
}

TEST_P(DilithiumAvx2VerifyTest, BasicSignVerify) {
  if (IsFipsModeEnabled()) {
    GTEST_SKIP() << "Test assumes kOnlyUseFips is false.";
  }

  const DilithiumTestCase& test_case = GetParam();

  // Generate key pair.
  util::StatusOr<
      std::pair<DilithiumPrivateKeyPqclean, DilithiumPublicKeyPqclean>>
      key_pair =
          DilithiumPrivateKeyPqclean::GenerateKeyPair(test_case.key_size);

  ASSERT_THAT(key_pair.status(), IsOk());

  // Create a new signer.
  util::StatusOr<std::unique_ptr<PublicKeySign>> signer =
      DilithiumAvx2Sign::New(key_pair->first);
  ASSERT_THAT(signer.status(), IsOk());

  // Create a new verifier.
  absl::StatusOr<std::unique_ptr<PublicKeyVerify>> verifier =
      DilithiumAvx2Verify::New(key_pair->second);
  ASSERT_THAT(verifier.status(), IsOk());

  // Sign a message.
  std::string message = "message to be signed";
  absl::StatusOr<std::string> signature = (*std::move(signer))->Sign(message);
  ASSERT_THAT(signature.status(), IsOk());

  // Verify signature.
  Status status = (*verifier)->Verify(*signature, message);
  EXPECT_THAT(status, IsOk());
}

